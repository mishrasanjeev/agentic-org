def _wrap_html(title: str, subtitle: str, body: str, generated_at: str) -> str:
    """Wrap report body in a full HTML document with AgenticOrg branding."""
    return (
        "<!DOCTYPE html><html lang='en'><head><meta charset='utf-8'>"
        f"<title>{html.escape(title)}</title>{_CSS}</head><body>"
        f"<div class='header'>"
        f"  <div class='logo'>AO</div>"
        f"  <div><div class='title'>{html.escape(title)}</div>"
        f"  <div class='subtitle'>{html.escape(subtitle)}</div></div>"
        f"</div>"
        f"{body}"
        f"<div class='footer'>Generated by AgenticOrg &middot; {html.escape(generated_at)}</div>"
        f"</body></html>"
    )


def _kpi_card(label: str, value: str, trend: float | None = None) -> str:
    """Render a single KPI card."""
    trend_html = ""
    if trend is not None:
        cls = "trend-up" if trend >= 0 else "trend-down"
        arrow = "&#9650;" if trend >= 0 else "&#9660;"
        trend_html = f"<div class='kpi-trend {cls}'>{arrow} {abs(trend):.1f}%</div>"
    return (
        f"<div class='kpi-card'>"
        f"  <div class='kpi-label'>{html.escape(label)}</div>"
        f"  <div class='kpi-value'>{html.escape(str(value))}</div>"
        f"  {trend_html}"
        f"</div>"
    )


def _table(headers: list[str], rows: list[list[str]]) -> str:
    """Render an HTML table."""
    hdr = "".join(f"<th>{html.escape(h)}</th>" for h in headers)
    body_rows = ""
    for row in rows:
        cells = "".join(f"<td>{html.escape(str(c))}</td>" for c in row)
        body_rows += f"<tr>{cells}</tr>"
    return f"<table><thead><tr>{hdr}</tr></thead><tbody>{body_rows}</tbody></table>"


def _inr(amount: int | float) -> str:
    """Format an Indian-rupee amount with commas."""
    s = f"{amount:,.0f}"
    return f"INR {s}"


# ---------------------------------------------------------------------------
# Generator class
# ---------------------------------------------------------------------------

class ReportGenerator:
    """Orchestrates report generation by dispatching to type-specific methods."""

    _DISPATCH: dict[str, str] = {
        "cfo_daily": "_generate_cfo_daily",
        "cmo_weekly": "_generate_cmo_weekly",
        "aging_report": "_generate_aging_report",
        "pnl_report": "_generate_pnl_report",
        "campaign_report": "_generate_campaign_report",
        "shadow_reconciliation": "_generate_shadow_reconciliation",
    }

    def generate(
        self,
        report_type: str,
        params: dict[str, Any],
        company_id: str = "default",
        tenant_id: str = "default",
    ) -> ReportOutput:
        """Entry point — resolve *report_type* and delegate."""
        method_name = self._DISPATCH.get(report_type)
        if method_name is None:
            supported = ", ".join(sorted(self._DISPATCH))
            msg = f"Unknown report type '{report_type}'. Supported: {supported}"
            raise ValueError(msg)

        method = getattr(self, method_name)
        log.info(
            "report_generate",
            report_type=report_type,
            company_id=company_id,
            tenant_id=tenant_id,
        )
        return method(params, company_id=company_id, tenant_id=tenant_id)

    # ------------------------------------------------------------------
    # CFO Daily
    # ------------------------------------------------------------------

    def _generate_cfo_daily(
        self,
        params: dict[str, Any],
        *,
        company_id: str = "default",
        tenant_id: str = "default",
    ) -> ReportOutput:
        data = self._fetch_cfo_kpis(company_id)
        now_str = datetime.now(UTC).strftime("%Y-%m-%d %H:%M UTC")

        kpis = "".join([
            "<div class='kpi-row'>",
            _kpi_card("Cash Runway", f"{data['cash_runway_months']:.1f} mo", data.get("cash_runway_trend")),
            _kpi_card("Monthly Burn", _inr(data["burn_rate"]), data.get("burn_rate_trend")),
            _kpi_card("DSO", f"{data['dso_days']} days", data.get("dso_trend")),
            _kpi_card("DPO", f"{data['dpo_days']} days", data.get("dpo_trend")),
            _kpi_card("Pending Approvals", str(data["pending_approvals_count"])),
            "</div>",
        ])

        bank_rows = [[b["account"], f"{b['currency']} {b['balance']:,.0f}"] for b in data["bank_balances"]]
        bank_table = _table(["Account", "Balance"], bank_rows)

        tax_rows = [[t["filing"], t["due_date"], t["status"].title()] for t in data["tax_calendar"]]
        tax_table = _table(["Filing", "Due Date", "Status"], tax_rows)

        body = f"{kpis}<h3>Bank Balances</h3>{bank_table}<h3>Tax Calendar</h3>{tax_table}"

        content_html = _wrap_html(
            "CFO Daily Briefing",
            f"Company: {company_id} | {now_str}",
            body,
            now_str,
        )

        return ReportOutput(
            content_html=content_html,
            content_data=data,
            report_type="cfo_daily",
        )

