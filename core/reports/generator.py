"""Report generation orchestrator.

Each report type has a dedicated private method that fetches KPI data
(currently demo data mirroring ``api/v1/kpis.py``), transforms it, and
produces a ``ReportOutput`` containing both structured data and an HTML
representation suitable for PDF rendering.
"""

