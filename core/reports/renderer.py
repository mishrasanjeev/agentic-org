"""Report renderers — PDF (fpdf2) and Excel (openpyxl).

Both functions accept a ``ReportOutput`` and an output path, write the
file, and return the path for downstream delivery.
"""

from __future__ import annotations

from datetime import UTC, datetime
from typing import Any

from core.reports.generator import ReportOutput

# ══════════════════════════════════════════════════════════════════════
# PDF renderer (fpdf2)
# ══════════════════════════════════════════════════════════════════════

def render_pdf(report: ReportOutput, output_path: str) -> str:
    """Render *report* as a professional PDF document.

    Layout
    ------
    - Header band with AO branding, report title, and date.
    - KPI summary cards (extracted from ``content_data``).
    - Data tables with borders and alternating row colours.
    - Footer with "Generated by AgenticOrg" and timestamp.
    """
    from fpdf import FPDF  # fpdf2

    class _AO_PDF(FPDF):  # noqa: N801
        """Custom FPDF subclass with AgenticOrg header/footer."""

        def __init__(self, title: str, generated_at: str) -> None:
            super().__init__(orientation="P", unit="mm", format="A4")
            self._ao_title = title
            self._ao_generated = generated_at

        # ── Header ───────────────────────────────────────────────
        def header(self) -> None:
            # Logo placeholder — gradient-like coloured rectangle
            self.set_fill_color(59, 130, 246)  # blue-500
            self.rect(10, 8, 12, 12, style="F")
            self.set_fill_color(139, 92, 246)  # purple-600
            self.rect(16, 8, 6, 12, style="F")
            self.set_font("Helvetica", "B", 10)
            self.set_text_color(255, 255, 255)
            self.set_xy(11, 11)
            self.cell(10, 6, "AO", align="C")

            # Title
            self.set_text_color(30, 41, 59)
            self.set_font("Helvetica", "B", 14)
            self.set_xy(26, 8)
            self.cell(0, 7, self._ao_title)

            # Date subtitle
            self.set_font("Helvetica", "", 9)
            self.set_text_color(100, 116, 139)
            self.set_xy(26, 15)
            self.cell(0, 5, self._ao_generated)

            self.ln(16)

        # ── Footer ───────────────────────────────────────────────
        def footer(self) -> None:
            self.set_y(-15)
            self.set_font("Helvetica", "I", 8)
            self.set_text_color(148, 163, 184)
            footer = f"AgenticOrg | {self._ao_generated} | Page {self.page_no()}/{{nb}}"
            self.cell(0, 10, footer, align="C")

    generated_at = report.generated_at or datetime.now(UTC).isoformat()

    pdf = _AO_PDF(
        title=_report_type_label(report.report_type),
        generated_at=generated_at,
    )
    pdf.alias_nb_pages()
    pdf.set_auto_page_break(auto=True, margin=20)
    pdf.add_page()

    data = report.content_data

    # ── KPI cards ────────────────────────────────────────────────
    kpi_items = _extract_kpi_items(report.report_type, data)
    if kpi_items:
        _pdf_kpi_row(pdf, kpi_items)

    # ── Data tables extracted from content_data ──────────────────
    tables = _extract_tables(report.report_type, data)
    for tbl_title, headers, rows in tables:
        pdf.ln(4)
        pdf.set_font("Helvetica", "B", 11)
        pdf.set_text_color(30, 41, 59)
        pdf.cell(0, 8, tbl_title, ln=True)
        _pdf_table(pdf, headers, rows)

    pdf.output(output_path)
    return output_path


def _report_type_label(report_type: str) -> str:
    labels: dict[str, str] = {
        "cfo_daily": "CFO Daily Briefing",
        "cmo_weekly": "CMO Weekly Report",
        "aging_report": "AR/AP Aging Report",
        "pnl_report": "Profit & Loss Report",
        "campaign_report": "Campaign Performance Report",
        "shadow_reconciliation": "Shadow Reconciliation Report",
    }
    return labels.get(report_type, report_type.replace("_", " ").title())


def _pdf_kpi_row(pdf: Any, items: list[tuple[str, str, float | None]]) -> None:
    """Draw a horizontal row of KPI cards."""
    card_w = min(38, (190 - 4 * (len(items) - 1)) / max(len(items), 1))
    start_x = pdf.get_x()
    start_y = pdf.get_y()

    for i, (label, value, trend) in enumerate(items):
        x = start_x + i * (card_w + 4)
        pdf.set_xy(x, start_y)

        # Card background
        pdf.set_fill_color(248, 250, 252)
        pdf.set_draw_color(226, 232, 240)
        pdf.rect(x, start_y, card_w, 20, style="DF")

        # Label
        pdf.set_xy(x + 2, start_y + 2)
        pdf.set_font("Helvetica", "", 7)
        pdf.set_text_color(100, 116, 139)
        pdf.cell(card_w - 4, 4, label[:18], ln=False)

        # Value
        pdf.set_xy(x + 2, start_y + 7)
        pdf.set_font("Helvetica", "B", 11)
        pdf.set_text_color(30, 41, 59)
        pdf.cell(card_w - 4, 6, value[:16], ln=False)

        # Trend
        if trend is not None:
            pdf.set_xy(x + 2, start_y + 14)
            pdf.set_font("Helvetica", "", 7)
            if trend >= 0:
                pdf.set_text_color(22, 163, 74)
                pdf.cell(card_w - 4, 4, f"+{trend:.1f}%", ln=False)
            else:
                pdf.set_text_color(220, 38, 38)
                pdf.cell(card_w - 4, 4, f"{trend:.1f}%", ln=False)

    pdf.set_y(start_y + 24)


def _pdf_table(pdf: Any, headers: list[str], rows: list[list[str]]) -> None:
    """Draw a table with header row and alternating row colours."""
    col_count = len(headers)
    col_w = 190.0 / max(col_count, 1)

    # Header
    pdf.set_font("Helvetica", "B", 9)
    pdf.set_fill_color(30, 41, 59)
    pdf.set_text_color(255, 255, 255)
    for h in headers:
        pdf.cell(col_w, 7, h[:24], border=1, fill=True)
    pdf.ln()

    # Rows
    pdf.set_font("Helvetica", "", 8)
    pdf.set_text_color(30, 41, 59)
    for idx, row in enumerate(rows):
        if idx % 2 == 0:
            pdf.set_fill_color(241, 245, 249)
        else:
            pdf.set_fill_color(255, 255, 255)
        for cell in row:
            pdf.cell(col_w, 6, str(cell)[:30], border=1, fill=True)
        pdf.ln()


